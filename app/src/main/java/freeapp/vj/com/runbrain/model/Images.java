package freeapp.vj.com.runbrain.model;

public class Images{

        private float x, y;
        private float width, height;

        public Images(float x, float y, float width, float height){

            this.x = x;
            this.y = y;
            this.width = width;
            this.height = height;

        }

        public float getX() {
            return x;
        }

        public float getY() {
            return y;
        }

        public float getWidth() {
            return width;
        }

        public float getHeight() {
            return height;
        }
}